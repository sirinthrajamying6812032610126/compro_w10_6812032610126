#include <stdio.h> // เรียกใช้ไลบรารีมาตรฐานสำหรับ Input/Output

int main() {  
    int num_students; // ประกาศตัวแปรเพื่อเก็บจำนวนนักเรียน
    int total_score = 0; // ประกาศและกำหนดค่าเริ่มต้นให้ตัวแปรเพื่อเก็บผลรวมของคะแนนทั้งหมด
    float average_score; // ประกาศตัวแปรเพื่อเก็บคะแนนเฉลี่ย (ใช้ float เพื่อให้มีทศนิยม)

    // --- รับข้อมูลจากผู้ใช้ ---
    printf("Enter number of students: "); // แสดงข้อความให้ผู้ใช้ป้อนจำนวนนักเรียน
    scanf("%d", &num_students); // อ่านค่าจำนวนนักเรียนที่ผู้ใช้ป้อนและเก็บไว้ในตัวแปร num_students

    // สร้างอาร์เรย์ (Array) เพื่อเก็บคะแนนของนักเรียนทุกคน
    // ขนาดของอาร์เรย์จะเท่ากับจำนวนนักเรียนที่ผู้ใช้ป้อน
    int scores[num_students];

    // ใช้ลูป (for loop) เพื่อรับคะแนนของนักเรียนแต่ละคน
    // ลูปจะทำงานซ้ำตามจำนวนนักเรียน
    for (int i = 0; i < num_students; i++) {
        printf("Enter score %d: ", i + 1); // แสดงข้อความให้ป้อนคะแนนของนักเรียนคนที่ i+1
        scanf("%d", &scores[i]); // อ่านค่าคะแนนและเก็บไว้ในอาร์เรย์ scores
        // เพิ่มคะแนนที่ป้อนเข้ามาในผลรวมคะแนนทั้งหมด
        total_score += scores[i]; 
    }

    // --- ประมวลผล ---
    // คำนวณคะแนนเฉลี่ย
    // (float)total_score เป็นการแปลงค่า total_score ให้เป็นทศนิยมก่อนการหาร
    average_score = (float)total_score / num_students;

    // --- แสดงผลลัพธ์ออกหน้าจอ ---
    printf("\nNumber of students = %d\n", num_students); // แสดงจำนวนนักเรียน
    // %.2f คือการแสดงผลลัพธ์ทศนิยม 2 ตำแหน่ง
    printf("Average score = %.2f\n", average_score); // แสดงคะแนนเฉลี่ย

    return 0; // คืนค่า 0 เพื่อบอกว่าโปรแกรมทำงานเสร็จสมบูรณ์
}